import type { AttackType, ArmorType } from "./damage-matrix";

export type Race = "human" | "orc" | "nightelf" | "undead";
export type UnitRole = "frontline" | "ranged" | "support" | "siege" | "caster" | "flying";
export type UnitTier = 1 | 2 | 3;

export interface Unit {
  id: string;
  name: string;
  race: Race;
  tier: UnitTier;
  role: UnitRole;
  attackType: AttackType;
  armorType: ArmorType;
  goldCost: number;
  lumberCost: number;
  foodCost: number;
  hp: number;
  damage: string;       // e.g. "12-13"
  dps: number;          // approximate DPS
  speed: string;        // "slow" | "medium" | "fast" | "very fast"
  range: string;        // "melee" | "ranged (X)"
  special: string[];    // notable abilities/traits
  counters: string[];   // unit IDs this unit is strong against
  weakTo: string[];     // unit IDs this unit is weak against
  description: string;
}

